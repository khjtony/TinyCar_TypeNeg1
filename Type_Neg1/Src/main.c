void vUARTTest(void *pvParameters){
    char words[16]="Helloworld\n";
    for(;;){
        HAL_UART_Transmit(&huart2, (uint8_t*)words, 11, 1/portTICK_PERIOD_MS);
        HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_13);
        vTaskDelay(1000/portTICK_PERIOD_MS);
    }
    
}


void vBTNTask(void *pvParameters){
	configASSERT( xBTNSemaphore);
	xSemaphoreTake( xBTNSemaphore, 0 );
	for(;;){
		xSemaphoreTake( xBTNSemaphore, portMAX_DELAY );
		uint16_t value = 1;
		xQueueSendToBack(msg_motor_DEBUG, &value, 0);
		HAL_GPIO_TogglePin(GPIOD, GPIO_PIN_15);
	}
	return;
}
